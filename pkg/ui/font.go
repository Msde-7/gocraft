// BitmapFont is a simple procedural bitmap font for UI text
type BitmapFont struct {
	textureID uint32
	charWidth int
	charHeight int
	cols       int
}

// NewBitmapFont creates a procedural 5x7 bitmap font texture
func NewBitmapFont() *BitmapFont {
	f := &BitmapFont{
		charWidth:  5,
		charHeight: 7,
		cols:       16,
	}

	rows := 6 // covers ASCII 32-127
	imgW := f.cols * f.charWidth
	imgH := rows * f.charHeight
	img := image.NewRGBA(image.Rect(0, 0, imgW, imgH))

	// Draw each printable ASCII character
	for ch := 32; ch < 128; ch++ {
		glyph := getGlyph(byte(ch))
		col := (ch - 32) % f.cols
		row := (ch - 32) / f.cols
		ox := col * f.charWidth
		oy := row * f.charHeight

		for y := 0; y < f.charHeight; y++ {
			for x := 0; x < f.charWidth; x++ {
				if y < len(glyph) && x < len(glyph[y]) && glyph[y][x] == 1 {
					img.Set(ox+x, oy+y, color.RGBA{255, 255, 255, 255})
				} else {
					img.Set(ox+x, oy+y, color.RGBA{0, 0, 0, 0})
				}
			}
		}
	}

	// Upload to GPU
	gl.GenTextures(1, &f.textureID)
	gl.BindTexture(gl.TEXTURE_2D, f.textureID)
	gl.TexParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, gl.NEAREST)
	gl.TexParameteri(gl.TEXTURE_2D, gl.TEXTURE_MAG_FILTER, gl.NEAREST)
	gl.TexImage2D(gl.TEXTURE_2D, 0, gl.RGBA,
		int32(imgW), int32(imgH), 0,
		gl.RGBA, gl.UNSIGNED_BYTE, gl.Ptr(img.Pix))

	return f
}

func (f *BitmapFont) Delete() {
	gl.DeleteTextures(1, &f.textureID)
}

